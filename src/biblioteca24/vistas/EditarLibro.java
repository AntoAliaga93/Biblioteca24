     private void armarCabecera(){
        modelo.addColumn("ISBN");
        modelo.addColumn("Título");
        modelo.addColumn("Autor");
        modelo.addColumn("Categoria");
        modelo.addColumn("Editorial");
        modelo.addColumn("Año");
        modelo.addColumn("Ejemplares");
        modelo.addColumn("Disponible");
        jTabla.setModel(modelo);
    }
      
     private void cargarTabla(){
        List<Libro> libros = lD.listarLibros();
        modelo.setRowCount(0);
        for (Libro libro : libros) {
            modelo.addRow(new Object[]{
            libro.getIsbn(), libro.getTitulo(), libro.getAutor(), libro.getCategoria(), 
                libro.getEditorial(), libro.getAnio(), libro.getCantEjemplar(), libro.isDisponible()
        });
        }
    }
     
     private void filtrarTabla() {
        // Obtén el texto ingresado en el JTextField de búsqueda
        String textF = tfTtitulo.getText();

        // Establece el filtro de la tabla
        RowFilter<DefaultTableModel, Object> rf = RowFilter.regexFilter("(?i)" +textF, 0, 1, 2, 3);
        rowSorter.setRowFilter(rf);
    }
