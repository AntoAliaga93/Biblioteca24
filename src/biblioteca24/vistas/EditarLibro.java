    private void jbAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAgregarActionPerformed
         if (validarCampos()) {
            try {
                // Obtener valores de los campos
                String titulo = tfTitulo.getText();
                String autor = tfAutor.getText();
                Categoria categoria = (Categoria) jcombo.getSelectedItem();
                String editorial = tfEditorial.getText();
                int anio = Integer.parseInt(tfAnio.getText());
                int cantidad = Integer.parseInt(tfCantidad.getText());
                boolean disponible = jbEstado.isSelected();

                Libro libro = new Libro(titulo, autor, categoria, editorial, anio, cantidad, disponible);
                lD.guardarLibro(libro);
                cargarTabla();
                limpiarCampos();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error al guardar el libro");
            }
        }
    }//GEN-LAST:event_jbAgregarActionPerformed
    private boolean validarCampos() {
        String titulo = tfTitulo.getText();
        String autor = tfAutor.getText();
        Categoria categoria = (Categoria) jcombo.getSelectedItem();
        String editorial = tfEditorial.getText();
        String anioStr = tfAnio.getText();
        String cantStr = tfCantidad.getText();

        if (titulo.isEmpty() || autor.isEmpty() || editorial.isEmpty() || anioStr.isEmpty() || cantStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No puede haber campos vacíos");
            return false;
        }

        if (autor.matches(".*\\d+.*")) {
            JOptionPane.showMessageDialog(this, "El campo de autor no puede contener números.");
            return false;
        }

        int anio, cantidad;

        try {
            anio = Integer.parseInt(anioStr);
            cantidad = Integer.parseInt(cantStr);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El año y la cantidad deben ser números válidos.");
            return false;
        }

        if (categoria == Categoria.SELECCIONAR) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar una categoría");
            return false;
        }

        return true;
    }

