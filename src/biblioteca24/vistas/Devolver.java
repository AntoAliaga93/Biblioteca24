    private void jbDevolverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbDevolverActionPerformed
        int filaSeleccionada = jTabla.getSelectedRow();

        if (filaSeleccionada >= 0) {
            int idEjemplar = Integer.parseInt(modelo.getValueAt(filaSeleccionada, 3).toString());
            Ejemplar ejemplar = eD.obtenerEjemplarXid(idEjemplar);
            pD.devolverLibro(ejemplar);

            // Recargar la tabla con los prestamos actuales
            cargarTablaPrestamos(prestamosActuales);
        } else {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione una fila de la tabla.");
        }
    }//GEN-LAST:event_jbDevolverActionPerformed

    private void jbBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscarActionPerformed
        String nroStr = tfNroSocio.getText();

        if (!nroStr.isEmpty()) {
            int nroSocio = Integer.parseInt(nroStr);

            if (lD.lectorExiste(nroSocio)) {
                List<Prestamo> prestamos = pD.obtenerPrestamosPorNroSocio(nroSocio);
                cargarTablaPrestamos(prestamos);
            } else {
                JOptionPane.showMessageDialog(this, "El número de socio ingresado no corresponde a un lector existente.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese un número de socio válido.");
        }
    }//GEN-LAST:event_jbBuscarActionPerformed

    private void jTablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTablaMouseClicked
        int filaSeleccionada = jTabla.getSelectedRow();

        if (filaSeleccionada >= 0) {
            Object idEjemplar = modelo.getValueAt(filaSeleccionada, 3); // Supongamos que la columna 3 contiene los idEjemplar

            if (idEjemplar != null) {
                int idEjemplarSeleccionado = Integer.parseInt(idEjemplar.toString());

                // Obtén el libro a partir del idEjemplar
                Ejemplar ejemplar = eD.obtenerEjemplarXid(idEjemplarSeleccionado);

                if (ejemplar != null) {
                    Libro libro = ejemplar.getLibro();

                    if (libro != null) {
                        String tituloLibro = libro.getTitulo();
                        JOptionPane.showMessageDialog(null, "Título del libro: " + tituloLibro);
                    } else {
                        JOptionPane.showMessageDialog(null, "No se encontró el libro correspondiente.");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "No se encontró el ejemplar correspondiente.");
                }
            }
        }

    }//GEN-LAST:event_jTablaMouseClicked

