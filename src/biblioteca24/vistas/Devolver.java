     private void armarCabecera(){
        modelo.addColumn("ID Prestamo");
        modelo.addColumn("Fecha Inicio");
        modelo.addColumn("Fecha Fin");
        modelo.addColumn("ID Ejemplar");
        modelo.addColumn("Nro Socio");
        modelo.addColumn("Estado");
        jTabla.setModel(modelo);
    }
    private void cargarTablaPrestamos(List<Prestamo> prestamos) {
        modelo.setRowCount(0);
        for (Prestamo p : prestamos) {
            modelo.addRow(new Object[]{
                p.getIdPrestamo(), p.getFechaInicio(), p.getFechaFin(), p.getEjemplar().getIdEjemplar(),
                p.getLector().getNroSocio(), p.isEstado()
            });
        }
    }

     
